package if_else_switch_7;

//* Question 8: Number Sign Check

public class NumberSignCheck {
    public static void main(String[] args) {
        int num = -5;

        if (num > 0) {
            System.out.println("Positive");
        } else if (num == 0) {
            System.out.println("Zero");
        } else {
            System.out.println("Negative");
        }
    }
}
