// Q4. Declare int num = -7. Print 'Positive' if num > 0, else print 'Non-positive'.

public class If_If_else_Q4 {
    public static void main(String[] args) {
        int num = -7;

        if(num > 0){
            System.out.println(num + " Is Positive");
        }else{
            System.out.println(num + " Non-positive");
        }
    }
}
