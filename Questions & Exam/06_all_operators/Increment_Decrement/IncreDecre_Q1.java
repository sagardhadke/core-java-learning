// Declare int i = 6. Apply i++ then i--. Print i. What do you get and why?

public class IncreDecre_Q1 {
    public static void main(String[] args) {
        int i = 6;
        i++; // 6 i is 6 because it's print the value then ++
        i--; // now the i is 7, and -- so i become an 6
        System.out.println(i); // 6
    }
}
