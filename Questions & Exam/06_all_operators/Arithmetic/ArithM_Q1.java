//* Declare two int variables a = 17 and b = 5. Print the result of a+b, a-b, a*b, a/b, and a%b on a single line separated by commas.

public class ArithM_Q1 {
    public static void main(String args[]) {
        int a = 17;
        int b = 5;

        System.out.println((a + b) + " , " + (a - b) + " , " + (a * b) + " , " + (a / b) + " , " + (a % b));
        // 22 , 12 , 85 , 3 , 2
    }
}