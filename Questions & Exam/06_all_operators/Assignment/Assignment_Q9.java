// Mixed Compound Assignment

// Declare:
// int num = 100;
//
// Apply the following in order:
//
// num /= 4;
// num += 15;
// num %= 7;
//
// Print num after every statement.
// Predict each value before running.

public class Assignment_Q9 {
    public static void main(String[] args) {
         int num = 100;

        num /= 4; // 25
        num += 15; // 40
        num %= 7; // 5

        System.out.println(num);

    }
}
