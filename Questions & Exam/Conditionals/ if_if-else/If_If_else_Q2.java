// Q2. Declare int a = 15, b = 10. Print the larger value using if-else.

public class If_If_else_Q2 {
    public static void main(String[] args) {
        int a = 15;
        int b = 10;

        if(a > b){
            System.out.println("A : " + a + " is Greater then " + b);
        }else{
            System.out.println("B : " + b + " is Greater then " + a);
        }
    }
}
