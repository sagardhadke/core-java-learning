// Q1. Declare int marks = 75. Print 'Pass' if marks >= 50, else print 'Fail'.

public class If_If_else_Q1 {
    public static void main(String[] args) {
        int marks = 75;

        if(marks >= 50){
            System.out.println("Pass");
        }else{
            System.out.println("Fail");
        }
    }
}
