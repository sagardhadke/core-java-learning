// Declare int j = 7. Apply j++ then ++j. Print j. Trace each step
public class IncreDecre_Q2 {
    public static void main(String[] args) {
        int j = 7;
        j++; // j is 7 because first print the value then ++
        ++j;// now j is 8 then again pre increment ++ now j become an 9
        System.out.println(j); // 9
    }
}
