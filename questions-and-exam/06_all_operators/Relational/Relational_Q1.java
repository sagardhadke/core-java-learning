// Declare int a = 10, b = 10. Print the result of all six relational operators (==, !=, <, >, <=, >=) one
// per line. Predict each before running.

public class Relational_Q1 {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;

        System.out.println(a == b); // Equal Equal TO => true
        System.out.println(a != b); // Not Equal TO => false
        System.out.println(a < b); // Less Then => false
        System.out.println(a > b); // Greater Then => false
        System.out.println(a <= b); // Less Then Equal To => true
        System.out.println(a >= b); // Greater Then Equal To => true
    }
}
