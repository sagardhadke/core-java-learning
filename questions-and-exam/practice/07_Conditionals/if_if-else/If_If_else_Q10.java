// Q10. Declare int a = 4, b = 4. Without using ==, determine if they are equal by checking if (a - b) gives
// zero. Print 'Equal' or 'Not Equal'.

public class If_If_else_Q10 {
    public static void main(String[] args) {
        int a = 4;
        int b = 4;

        if(a - b == 0){
            System.out.println("Equal");
        }else {
            System.out.println("Not Equal");
        }
    }
}
