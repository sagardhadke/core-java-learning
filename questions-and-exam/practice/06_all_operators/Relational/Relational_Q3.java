// Declare int x = 100. Write and print three separate boolean expressions: x >= 100, x > 100, x <=
// 100.

public class Relational_Q3 {
    public static void main(String[] args) {
        int x = 100;

        System.out.println(x >= 100); // true
        System.out.println(x > 100); // false
        System.out.println(x <= 100); // true
    }
}
