// Declare int a = 9, b = 2. Print a/b. Then declare double c = 9, d = 2. Print c/d. Observe and comment on the difference.

public class ArithM_Q4 {
    public static void main(String[] args) {
        int a = 9;
        int b = 2;

        System.out.println(a / b); // 4
        double c = 9;
        double d = 2;
        System.out.println(c / d); // 4.5
    }
}