// boolean a = true, b = false; Print: a && b, a || b, !a, !b, a && !b, !a || b.

public class Logical_Q6 {
    public static void main(String[] args) {
        boolean a = true, b = false;
        System.out.println(a && b);
        System.out.println(a || b);
        System.out.println(!a);
        System.out.println(!b);
        System.out.println(a && !b);
        System.out.println(!a || b);
    }
}
