// Q5. Declare int num = 14. Check if it is even using num % 2 == 0. Print 'Even' or 'Odd'.

public class If_If_else_Q5 {
    public static void main(String[] args) {
        int num = 14;

        if(num % 2 == 0){
            System.out.println(num + " is Even");
        }else{
            System.out.println(num + " is Odd");
        }
    }
}
